/**
 * A Bootstrap Extra Component that displays a progress bar.
 * 
 * DEPRECATED: Use bootstrapextracomponents-progressbar component instead
 */

/**
 * Set the styleclasses that should be applied to this component.
 */
var styleClass;

/**
 * The current progress value as a float.
 */
var value;

/**
 * The type of the progress bar. Possible values: "info", "success", "warning", "danger".
 */
var type;

/**
 * When true, enables animation for the progress transitions.
 */
var animate;

/**
 * When true, displays the progress value on the progress bar.
 */
var showValue;

/**
 * The maximum value for the progress bar.
 */
var max;

/**
 * Tab sequence order for keyboard navigation.
 */
var tabSeq;

/**
 * Tooltip text displayed when hovering over the progress bar.
 */
var toolTipText;

/**
 * Whether the button is visible or not
 */
var visible;


